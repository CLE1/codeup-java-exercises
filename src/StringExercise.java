public class StringExercise {
    public static void main(String[] args) {
        String PinkFloyd = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(PinkFloyd);

        String random = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(random);

        String drive = "In windows, the main drive is usually C:\\";
        System.out.println(drive);

        String slashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(slashes);



    }
}
